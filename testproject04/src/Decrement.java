
public class Decrement {

	public static void main(String[] args) {
		// 加算子、減算子
		// 前置減算子
		
		// --a・・・変数から 1 を引いたモノ表示
		int a = 10;
		System.out.println(--a +"\n");
		
		// 続けて、--aと記述すると、このように1つずつ数を減少させて表示できる。 
		int a2 = 10;
		System.out.println(--a2);
		System.out.println(--a2);
		System.out.println(--a2);
		System.out.println(--a2);

	}

}
