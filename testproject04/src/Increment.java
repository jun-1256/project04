
public class Increment {

	public static void main(String[] args) {
		// 加算子、減算子
		// 前置加算子
		
		// ++a・・・変数a に 1 を加えた値を表示
		int a = 10;
		System.out.println(++a + "\n");
		
		int a2 = 5;
		System.out.println(++a2 + "\n");
		
		// 続けて、++aと記述すると、このように1つずつ数を増加させて表示できる。
		int a3 = 10;
		System.out.println(++a3);
		System.out.println(++a3);
		System.out.println(++a3);
		System.out.println(++a3);

	}

}
